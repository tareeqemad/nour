<div class="guide-section">
    <div class="general-card">
        <div class="section-header">
            <h5>
                <i class="bi bi-list-check"></i>
                دليل خطوات العمل الشامل للمشغل
            </h5>
        </div>
        <div class="alert alert-primary mb-4">
            <h6 class="alert-heading">
                <i class="bi bi-lightbulb"></i>
                ابدأ من هنا
            </h6>
            <p class="mb-0">
                اتبع هذه الخطوات بالترتيب لإعداد حسابك بشكل صحيح والبدء في استخدام المنصة بكفاءة.
                كل خطوة مهمة وتؤثر على الخطوات التالية.
            </p>
        </div>
        
        <ol class="steps-list">
            <div class="step-item">
                <h4>الخطوة 1: إعداد ملف المشغل (إلزامي)</h4>
                <p class="text-muted mb-2">
                    هذه الخطوة <strong>إلزامية</strong> ويجب إكمالها قبل أي خطوة أخرى. بدون إكمال ملف المشغل، 
                    لن تتمكن من إضافة وحدات التوليد أو المولدات.
                </p>
                <ul class="text-muted mb-0">
                    <li>اذهب إلى <strong>المشغل → ملف المشغل</strong></li>
                    <li>أكمل جميع البيانات المطلوبة (المميزة بعلامة <span class="text-danger">*</span>):</li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>الاسم بالعربي واسم المالك بالعربي</li>
                            <li>المحافظة والمدينة</li>
                            <li>العنوان التفصيلي</li>
                            <li>معلومات الاتصال (الهاتف، البريد الإلكتروني)</li>
                            <li>أي بيانات أخرى مطلوبة</li>
                        </ul>
                    </li>
                    <li>انقر على <strong>حفظ</strong> بعد إكمال جميع البيانات</li>
                    <li>تأكد من ظهور رسالة نجاح قبل الانتقال للخطوة التالية</li>
                </ul>
            </div>
            
            <div class="step-item">
                <h4>الخطوة 2: إضافة وحدات التوليد</h4>
                <p class="text-muted mb-2">
                    بعد إكمال ملف المشغل، يمكنك إضافة وحدات التوليد التابعة لك. 
                    كل وحدة توليد تمثل موقعاً جغرافياً محدداً يحتوي على مولدات.
                </p>
                <ul class="text-muted mb-0">
                    <li>اذهب إلى <strong>وحدات التوليد → إضافة وحدة توليد جديدة</strong></li>
                    <li>أدخل البيانات الأساسية:</li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>اسم وحدة التوليد</li>
                            <li>المحافظة والمدينة</li>
                            <li>العنوان التفصيلي</li>
                            <li>رقم الهاتف (إن وجد)</li>
                        </ul>
                    </li>
                    <li><strong>تحديد الموقع الجغرافي (مهم جداً):</strong></li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>افتح تبويب <strong>الموقع</strong></li>
                            <li>انقر على الخريطة لتحديد موقع وحدة التوليد</li>
                            <li>أدخل <strong>اسم المنطقة</strong> (مثل: "منطقة الشمال")</li>
                            <li>أدخل <strong>مساحة المنطقة</strong> بالكيلومتر المربع (من 0.1 إلى 360 كم²)</li>
                            <li>النظام سيرسم دائرة على الخريطة تمثل المنطقة المخصصة لك</li>
                            <li>النظام سيمنع إضافة وحدات أخرى في نفس المنطقة أو مناطق متداخلة</li>
                        </ul>
                    </li>
                    <li>احفظ الوحدة بعد التأكد من جميع البيانات</li>
                </ul>
            </div>
            
            <div class="step-item">
                <h4>الخطوة 3: إضافة المولدات</h4>
                <p class="text-muted mb-2">
                    بعد إضافة وحدة التوليد، يمكنك إضافة المولدات التابعة لها. 
                    يمكنك إضافة عدة مولدات لكل وحدة توليد.
                </p>
                <ul class="text-muted mb-0">
                    <li>اذهب إلى <strong>المولدات → إضافة مولد جديد</strong></li>
                    <li>اختر <strong>وحدة التوليد</strong> التابعة لها</li>
                    <li>أدخل بيانات المولد:</li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>رقم المولد (يمكنك استخدام "توليد رقم تلقائي")</li>
                            <li>نوع المولد (ديزل، غاز، إلخ)</li>
                            <li>القدرة (بالكيلووات)</li>
                            <li>الشركة المصنعة</li>
                            <li>رقم التسلسلي</li>
                            <li>تاريخ الشراء</li>
                            <li>أي بيانات أخرى مطلوبة</li>
                        </ul>
                    </li>
                    <li>احفظ المولد بعد التأكد من جميع البيانات</li>
                    <li>يمكنك إضافة عدة مولدات لنفس وحدة التوليد</li>
                </ul>
            </div>
            
            <div class="step-item">
                <h4>الخطوة 4: إضافة فريق العمل</h4>
                <p class="text-muted mb-2">
                    يمكنك إضافة أعضاء فريق العمل لمساعدتك في إدارة العمليات. 
                    يمكنك إضافة فني مشغل ومحاسب، وإدارة صلاحيات كل عضو.
                </p>
                <ul class="text-muted mb-0">
                    <li>اذهب إلى <strong>فريق العمل → إضافة مستخدم جديد</strong></li>
                    <li>اختر <strong>الدور</strong>: فني مشغل أو محاسب</li>
                    <li>أدخل بيانات العضو:</li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>الاسم بالعربي</li>
                            <li>الاسم بالإنجليزي (للتوليد التلقائي لـ username)</li>
                            <li>رقم الجوال (اختياري - لإرسال SMS)</li>
                        </ul>
                    </li>
                    <li>انقر على <strong>حفظ</strong></li>
                    <li>سيتم توليد username و password تلقائياً</li>
                    <li>إذا أدخلت رقم جوال، سيتم إرسال SMS تلقائياً بالبيانات</li>
                    <li>يمكنك إدارة صلاحيات كل عضو من خلال <strong>الصلاحيات</strong></li>
                </ul>
            </div>
            
            <div class="step-item">
                <h4>الخطوة 5: تسجيل السجلات اليومية</h4>
                <p class="text-muted mb-2">
                    بعد إعداد جميع البيانات الأساسية، يمكنك البدء في تسجيل السجلات اليومية. 
                    هذه السجلات مهمة جداً لتتبع الأداء والامتثال.
                </p>
                <ul class="text-muted mb-0">
                    <li><strong>سجلات التشغيل:</strong></li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>اذهب إلى <strong>سجلات التشغيل → إضافة سجل جديد</strong></li>
                            <li>اختر وحدة التوليد والمولد</li>
                            <li>أدخل البيانات: تاريخ التشغيل، وقت البدء والانتهاء، الطاقة المنتجة، الوقود المستهلك</li>
                            <li>احفظ السجل</li>
                        </ul>
                    </li>
                    <li><strong>سجلات الصيانة:</strong></li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>اذهب إلى <strong>سجلات الصيانة → إضافة سجل جديد</strong></li>
                            <li>اختر نوع الصيانة (دورية، طارئة، وقائية)</li>
                            <li>أدخل تفاصيل الصيانة والتكلفة</li>
                            <li>احفظ السجل</li>
                        </ul>
                    </li>
                    <li><strong>الامتثال والسلامة:</strong></li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>اذهب إلى <strong>الامتثال والسلامة → إضافة سجل جديد</strong></li>
                            <li>سجل أي تقارير أو فحوصات متعلقة بالامتثال والسلامة</li>
                        </ul>
                    </li>
                    <li><strong>كفاءة الوقود:</strong></li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>اذهب إلى <strong>كفاءة الوقود → إضافة سجل جديد</strong></li>
                            <li>سجل بيانات كفاءة الوقود للمولدات</li>
                        </ul>
                    </li>
                </ul>
            </div>
            
            <div class="step-item">
                <h4>الخطوة 6: إدارة أسعار التعرفة</h4>
                <p class="text-muted mb-2">
                    يمكنك إدارة أسعار التعرفة الكهربائية لكل وحدة توليد. 
                    يمكنك تحديد أسعار مختلفة لفترات مختلفة.
                </p>
                <ul class="text-muted mb-0">
                    <li>اذهب إلى <strong>المشغل → أسعار التعرفة</strong></li>
                    <li>اختر وحدة التوليد</li>
                    <li>أضف سعر تعرفة جديد مع تحديد:</li>
                    <li style="list-style: none; padding-right: 1.5rem;">
                        <ul>
                            <li>تاريخ البدء والانتهاء</li>
                            <li>السعر لكل كيلووات ساعة</li>
                            <li>أي تفاصيل أخرى</li>
                        </ul>
                    </li>
                </ul>
            </div>
            
            <div class="step-item">
                <h4>الخطوة 7: المراقبة والتحليل</h4>
                <p class="text-muted mb-0">
                    استخدم لوحة التحكم والتقارير لمراقبة أداء مولداتك واتخاذ قرارات مدروسة. 
                    يمكنك عرض الإحصائيات الشاملة، مراقبة كفاءة الوقود، ومراجعة جميع السجلات التاريخية.
                </p>
            </div>
        </ol>
    </div>
</div>
